/**
 * Upload helpers for generated media results and remote URL downloads.
 * Handles S3 upload of generated assets and downloading remote URLs to temp files.
 * @see docs/api-contracts/storage-provider.md
 */

import { randomUUID } from 'node:crypto'
import { writeFile, unlink } from 'node:fs/promises'
import { extname, join } from 'node:path'
import { tmpdir } from 'node:os'

import type { AssetRecord } from '../../../../shared/assets'
import type { AssetRepository } from '../db/repositories/asset.repo'
import { getCurrentStorageConfig } from '../ipc/storage.handler'
import { storageFactory } from '../storage/storage-factory'

function inferExtension(mimeType: string | undefined, fallback: string): string {
  if (mimeType === 'image/png') return '.png'
  if (mimeType === 'image/jpeg') return '.jpg'
  if (mimeType === 'image/webp') return '.webp'
  if (mimeType === 'video/mp4') return '.mp4'
  return fallback
}

/**
 * Downloads a remote URL to a temporary file.
 * @param url - Remote URL to download.
 * @returns Absolute path to the temporary file.
 * @throws Error when the download fails.
 */
export async function downloadToTemp(url: string): Promise<string> {
  const response = await fetch(url)
  if (!response.ok) {
    throw new Error(`Failed to download ${url}: ${response.status}`)
  }
  const buffer = Buffer.from(await response.arrayBuffer())
  const ext = extname(new URL(url).pathname) || '.png'
  const tempPath = join(tmpdir(), `cc-${randomUUID()}${ext}`)
  await writeFile(tempPath, buffer)
  return tempPath
}

/**
 * Writes a buffer to a temporary file.
 * @param bytes - Byte content to write.
 * @param mimeType - MIME type used to infer file extension.
 * @returns Absolute path to the temporary file.
 */
export async function writeBufferToTemp(bytes: Uint8Array, mimeType: string): Promise<string> {
  const ext = inferExtension(mimeType, '.bin')
  const tempPath = join(tmpdir(), `cc-${randomUUID()}${ext}`)
  await writeFile(tempPath, Buffer.from(bytes))
  return tempPath
}

/**
 * Uploads a generated asset's local file to S3 and updates the asset record.
 * When no storage config is set, silently returns (backward-compatible: local-only mode).
 * @param asset - The persisted asset record (must have relativePath and id).
 * @param assetRoot - Absolute path to the asset storage root.
 * @param assets - Asset repository for updating the cloud URL.
 * @returns The cloud URL if uploaded, or undefined if no storage configured.
 */
export async function uploadGeneratedAssetToCloud(
  asset: AssetRecord,
  assetRoot: string,
  assets: AssetRepository
): Promise<string | undefined> {
  const storageConfig = getCurrentStorageConfig()
  if (!storageConfig) {
    return undefined
  }

  const provider = storageFactory.create(storageConfig)
  const ext = extname(asset.relativePath) || inferExtension(asset.metadata.mimeType, '.png')
  const key = `outputs/${new Date().toISOString().slice(0, 7)}/${randomUUID()}${ext}`

  // Resolve local file path from asset root + relative path
  const localPath = join(assetRoot, asset.relativePath)
  const cloudUrl = await provider.upload(localPath, key)

  // Update the asset record with the cloud URL and S3 key
  assets.updateUrl(asset.id, cloudUrl, key)

  return cloudUrl
}

/**
 * Uploads generated bytes directly to S3 (used when the bytes are in-memory).
 * When no storage config is set, silently returns (backward-compatible: local-only mode).
 * @param bytes - Generated media bytes.
 * @param assetId - ID of the persisted asset record.
 * @param mimeType - MIME type for extension inference.
 * @param assets - Asset repository for updating the cloud URL.
 * @returns The cloud URL if uploaded, or undefined if no storage configured.
 */
export async function uploadBytesToCloud(
  bytes: Uint8Array,
  assetId: string,
  mimeType: string | undefined,
  assets: AssetRepository
): Promise<string | undefined> {
  const storageConfig = getCurrentStorageConfig()
  if (!storageConfig) {
    return undefined
  }

  const provider = storageFactory.create(storageConfig)
  const ext = inferExtension(mimeType, '.png')
  const key = `outputs/${new Date().toISOString().slice(0, 7)}/${randomUUID()}${ext}`

  // Write to temp file, upload, then clean up
  const tempPath = await writeBufferToTemp(bytes, mimeType ?? 'application/octet-stream')
  let cloudUrl: string
  try {
    cloudUrl = await provider.upload(tempPath, key)
  } finally {
    await unlink(tempPath).catch(() => {})
  }

  // Update the asset record
  assets.updateUrl(assetId, cloudUrl, key)

  return cloudUrl
}
